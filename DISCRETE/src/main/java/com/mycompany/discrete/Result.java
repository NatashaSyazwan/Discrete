     public void showAllPermutations() {
        StringBuilder sb = new StringBuilder();
        sb.append("All distinct permutations:\n");
        for (String permutation : storedPermutations) {
            sb.append(permutation).append("\n");
        }
        txtResults.append(sb.toString());
    }

    public void setStoredPermutations(Set<String> permutations) {
        this.storedPermutations = permutations;
    }

    public void setResults(String resultsText) {
        txtResults.setText(resultsText); // Set the text to replace existing content
    }

    public JTextArea getTxtResults() {
        return txtResults;
    }


   
